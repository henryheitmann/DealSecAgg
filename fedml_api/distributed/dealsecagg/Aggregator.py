import copy
import logging
import random
from collections import OrderedDict

import numpy as np
import torch
import wandb

from fedml_api.distributed.utils.masking import add_models, subtract_models, divide_model
from fedml_api.distributed.utils.utils import transform_array_to_tensor, convert_int16_to_float32, \
    convert_int8_to_float32


class Aggregator(object):
    def __init__(
            self,
            train_global,
            test_global,
            all_train_data_num,
            train_data_local_dict,
            test_data_local_dict,
            train_data_local_num_dict,
            worker_num,
            device,
            args,
            model_trainer
    ):
        self.trainer = model_trainer

        self.args = args
        self.train_global = train_global
        self.test_global = test_global
        self.val_global = self._generate_validation_set()
        self.all_train_data_num = all_train_data_num

        self.train_data_local_dict = train_data_local_dict
        self.test_data_local_dict = test_data_local_dict
        self.train_data_local_num_dict = train_data_local_num_dict

        self.worker_num = worker_num
        self.device = device
        self.model_dict = dict()
        self.sample_num_dict = dict()
        self.aggregated_masks = []
        self.quantization_datatype = args.quantization

    def get_global_model_params(self):
        return self.trainer.get_model_params()

    def set_global_model_params(self, model_parameters):
        self.trainer.set_model_params(model_parameters)

    def add_masks(self, masks):
        self.aggregated_masks = masks

    def aggregate_model_reconstruction(self):
        logging.info("Server starts the reconstruction of aggregate_model")

        aggregated_model = OrderedDict()

        for idx, key in enumerate(self.model_dict):
            if idx == 0:
                aggregated_model = self.model_dict[key]
            else:
                aggregated_model = add_models(aggregated_model, self.model_dict[key])

        for mask in self.aggregated_masks:
            aggregated_model = subtract_models(aggregated_model, mask)

        averaged_params = divide_model(aggregated_model, len(self.model_dict))

        if self.quantization_datatype == 'int8':
            averaged_params = convert_int8_to_float32(averaged_params)
        elif self.quantization_datatype == 'int16':
            averaged_params = convert_int16_to_float32(averaged_params)

        model_params = transform_array_to_tensor(averaged_params)
        self.set_global_model_params(model_params)

        return averaged_params

    def add_local_trained_result(self, index, model_params, sample_num):
        logging.info("add_model. index = %d" % index)
        self.model_dict[index] = model_params
        self.sample_num_dict[index] = sample_num

    def client_sampling(self, round_idx, client_num_in_total, client_num_per_round):
        if client_num_in_total == client_num_per_round:
            client_indexes = [client_index for client_index in range(client_num_in_total)]
        else:
            num_clients = min(client_num_per_round, client_num_in_total)
            np.random.seed(round_idx)  # make sure for each comparison, we are selecting the same clients each round
            client_indexes = np.random.choice(range(client_num_in_total), num_clients, replace=False)
        logging.info("client_indexes = %s" % str(client_indexes))
        return client_indexes

    def _generate_validation_set(self, num_samples=10000):
        if self.args.dataset.startswith("stackoverflow"):
            test_data_num = len(self.test_global.dataset)
            sample_indices = random.sample(range(test_data_num), min(num_samples, test_data_num))
            subset = torch.utils.data.Subset(self.test_global.dataset, sample_indices)
            sample_testset = torch.utils.data.DataLoader(subset, batch_size=self.args.batch_size)
            return sample_testset
        else:
            return self.test_global

    def test_on_server_for_all_clients(self, round_idx):

        if self.trainer.test_on_the_server(
                self.train_data_local_dict, self.test_data_local_dict, self.device, self.args
        ):
            return

        if round_idx % self.args.frequency_of_the_test == 0 or round_idx == self.args.comm_round - 1:
            logging.info("################test_on_server_for_all_clients : {}".format(round_idx))

            # test data
            test_num_samples = []
            test_tot_corrects = []
            test_losses = []

            if round_idx == self.args.comm_round - 1:
                metrics = self.trainer.test(self.test_global, self.device, self.args)
            else:
                metrics = self.trainer.test(self.val_global, self.device, self.args)

            test_tot_correct, test_num_sample, test_loss = (
                metrics["test_correct"],
                metrics["test_total"],
                metrics["test_loss"],
            )
            test_tot_corrects.append(copy.deepcopy(test_tot_correct))
            test_num_samples.append(copy.deepcopy(test_num_sample))
            test_losses.append(copy.deepcopy(test_loss))

            # test on test dataset
            test_acc = sum(test_tot_corrects) / sum(test_num_samples)
            test_loss = sum(test_losses) / sum(test_num_samples)
            wandb.log({"Test/Acc": test_acc, "round": round_idx})
            wandb.log({"Test/Loss": test_loss, "round": round_idx})
            stats = {"test_acc": test_acc, "test_loss": test_loss}
            logging.info(stats)
